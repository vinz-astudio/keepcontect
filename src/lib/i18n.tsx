const DICTS: Record<Lang, Record<I18nKey, string>> = { zh, en }

function detectLang(): Lang {
  if (typeof localStorage === 'undefined') return 'en'
  const saved = localStorage.getItem(LANG_KEY)
  if (saved === 'zh' || saved === 'en') return saved
  return 'en' // 默认英文为主;用户手动切换后持久化
}

let currentLang: Lang = detectLang()

export function getLang(): Lang {
  return currentLang
}

/** 非 React 环境可用的翻译(如分享文案) */
export function translate(
  key: I18nKey,
  params?: Record<string, string | number>,
): string {
  let s: string = DICTS[currentLang][key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{${k}}`, String(v))
    }
  }
  return s
}

interface I18nValue {
  lang: Lang
  t: (key: I18nKey, params?: Record<string, string | number>) => string
  setLang: (l: Lang) => void
}

const I18nCtx = createContext<I18nValue | undefined>(undefined)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(currentLang)

  const value: I18nValue = {
    lang,
    t: translate,
    setLang: (l) => {
      currentLang = l
      if (typeof localStorage !== 'undefined') localStorage.setItem(LANG_KEY, l)
      setLangState(l)
    },
  }

  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>
}

// eslint-disable-next-line react-refresh/only-export-components
export function useI18n(): I18nValue {
  const v = useContext(I18nCtx)
  if (!v) throw new Error('useI18n must be used within <I18nProvider>')
  return v
}

/** 语言切换按钮(中 ⇄ EN) */
export function LangToggle({ className, style }: { className?: string; style?: React.CSSProperties }) {
  const { lang, setLang } = useI18n()
  return (
    <button
      type="button"
      className={className}
      style={style}
      onClick={() => setLang(lang === 'zh' ? 'en' : 'zh')}
      aria-label="Switch language"
    >
      {lang === 'zh' ? 'EN' : '中文'}
    </button>
  )
}
